import filterPlanet from '../../utils/filterPlanet';

const filteredPlanet = {
  id: 'test',
  climate: 'test',
  created: 'test',
  diameter: 'test',
  edited: 'test',
  gravity: 'test',
  name: 'test',
  orbital_period: 'test',
  population: 'test',
  rotation_period: 'test',
  surface_water: 'test',
  terrain: 'test',
};

const notFilteredPlanet = {
  ...filteredPlanet,
  residents: ['test', 'test'],
  films: ['test', 'test'],
  url: '',
};

describe('filterPlanet', () => {
  it('Should return the filtered object', () => {
    const planet = filterPlanet(notFilteredPlanet);
    const planetObject = Object.fromEntries(planet);
    expect(planetObject).toEqual(filteredPlanet);
  });
});
